export function getByAgency(agency: string): readonly Mission[] {
  return MISSIONS.filter((m) => m.agency === agency);
}

export function getByDecade(decade: number): readonly Mission[] {
  return MISSIONS.filter((m) => Math.floor(m.year / 10) * 10 === decade);
}

export function getByDestination(dest: string): readonly Mission[] {
  return MISSIONS.filter((m) => m.destination === dest);
}

export function getByType(type: string): readonly Mission[] {
  return MISSIONS.filter((m) => m.type === type);
}

export const AGENCIES: readonly string[] = [...new Set(MISSIONS.map((m) => m.agency))].sort();
export const DESTINATIONS: readonly string[] = [...new Set(MISSIONS.map((m) => m.destination))].sort();
export const TYPES: readonly string[] = [...new Set(MISSIONS.map((m) => m.type))].sort();
export const DECADES: readonly string[] = [
  ...new Set(MISSIONS.map((m) => `${String(Math.floor(m.year / 10) * 10)}s`)),
].sort();

